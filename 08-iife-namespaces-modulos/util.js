/*
 * Modulos: são pedaços de codigos encapsulados que podem ser importados e expõem aluns métodos para serem utilizados
 * Objet Shorthand: nome da propriedade com o mesmo nome de valor, não é necessario passar o valor
 * Vantagens: encapsulamento, reutilizavel, abstrato, manutenivel e legibilidade
 */

const value = 5;

const helloWorld = function () {
  alert("Olá Mundo!");
};

const multiplica = function (value) {
  alert(value * valor);
};

